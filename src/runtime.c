inline int add(int num1, int num2)
{
    return num1 + num2;
}

inline int subtract(int num1, int num2)
{
    return num1 - num2;
}

inline int multiply(int num1, int num2)
{
    return num1 * num2;
}

inline int quotient(int num1, int num2)
{
    return num1 / num2;
}

inline int remainder(int num1, int num2)
{
    return num1 % num2;
}
